      :color="showAddTask ? 'red' : 'green'"
    />
  </header>
</template>

<script>
import Button from "./Button";

export default {
  name: "Header",
  props: {
    title: String,
    showAddTask: Boolean,
  },
  components: {
    Button,
  },
  // computed is used when you have two elements and want to join them together, like first name and last name
  computed: {
    homePage() {
      // this.$router.push() would be the redirect. The this.$route.path is just to grab the URL.
      if (this.$route.path === "/") {
        return true;
      } else {
        return false;
      }
    },
  },
};
</script>

